#pragma once

// =======================================================================
//
namespace math
{

// =======================================================================
//
class Vec2
{
public:

    // -----------------------------------------------------------------
    //
    Vec2();

    // -----------------------------------------------------------------
    //
    constexpr Vec2( double x,
                    double y );

    // -----------------------------------------------------------------
    //
    void reset( double x,
                double y );

    // -----------------------------------------------------------------
    //
    void print() const;

    // -----------------------------------------------------------------
    //
    double operator[]( size_t i ) const;

    // -----------------------------------------------------------------
    //
    ~Vec2() = default;
    Vec2( const Vec2& ) = default;
    Vec2( Vec2&& ) = default;
    Vec2& operator=( const Vec2& ) = default;
    Vec2& operator=( Vec2&& ) = default;

    // -----------------------------------------------------------------
    //
    double x_;
    double y_;
};

}
